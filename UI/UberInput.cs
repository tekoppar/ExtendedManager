        private void uberGroupId_SelectedIndexChanged(object sender, EventArgs e) {
            this.GenerateUberList();
        }

        private void uberId_SelectedIndexChanged(object sender, EventArgs e) {
            var uberGroup = UberInput.UberStates[this.uberGroupId.SelectedItem.ToString()];

            if (uberGroup != null) {
                var uberId = uberGroup.UberIDs[this.uberId.SelectedItem.ToString()];

                if (uberId != null) {
                    switch (uberId.Type) {
                        case "bool":
                            this.uberIdValueBool.Visible = true;
                            this.uberIdValueInt.Visible = false;
                            this.uberIdValueFloat.Visible = false;
                            this.uberIdValueByte.Visible = false;
                            break;

                        case "int":
                            this.uberIdValueBool.Visible = false;
                            this.uberIdValueInt.Visible = true;
                            this.uberIdValueFloat.Visible = false;
                            this.uberIdValueByte.Visible = false;
                            break;

                        case "float":
                            this.uberIdValueBool.Visible = false;
                            this.uberIdValueInt.Visible = false;
                            this.uberIdValueFloat.Visible = true;
                            this.uberIdValueByte.Visible = false;
                            break;

                        case "byte":
                            this.uberIdValueBool.Visible = false;
                            this.uberIdValueInt.Visible = false;
                            this.uberIdValueFloat.Visible = false;
                            this.uberIdValueByte.Visible = true;
                            break;
                    }
                }
            }
        }

        public void ChangeUberGroup(string uberGroup) {
            if (uberGroup.Length > 0) {
                this.uberGroupId.SelectedItem = uberGroup;
            }
        }

        public void ChangeUber(string uber) {
            if (uber.Length > 0) {
                this.uberId.SelectedItem = uber;
            }
        }

        public void SetValue(UberValue uberValue, UberStateType uberStateType) {
            switch (uberStateType) {
                case UberStateType.SerializedBooleanUberState:
                    this.uberIdValueBool.Checked = uberValue.Bool;
                    break;

                case UberStateType.SerializedIntUberState:
                    this.uberIdValueInt.Value = uberValue.Int;
                    break;

                case UberStateType.SerializedFloatUberState:
                    this.uberIdValueFloat.Value = (decimal)uberValue.Float;
                    break;

                case UberStateType.SerializedByteUberState:
                    this.uberIdValueByte.Value = (int)uberValue.Byte;
                    break;
            }
        }

        private void remove_Click(object sender, EventArgs e) {
            UberstateEditor uberstateEditor = this.ParentForm as UberstateEditor;

            if (uberstateEditor != null) {
                uberstateEditor.RemoveUberstate(this);
            }
        }
